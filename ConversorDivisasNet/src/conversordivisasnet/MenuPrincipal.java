    private void linkedinLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_linkedinLinkMouseClicked
        
        if(java.awt.Desktop.isDesktopSupported()){
            java.awt.Desktop desktop = java.awt.Desktop.getDesktop();
            
            if(desktop.isSupported(java.awt.Desktop.Action.BROWSE)){
                try{
                    java.net.URI uri = new java.net.URI("https://www.linkedin.com/in/jose-eduardo-rodriguez-antunez/");
                    desktop.browse(uri);
                } catch(URISyntaxException | IOException ex) {}
            }
        }
    }//GEN-LAST:event_linkedinLinkMouseClicked

